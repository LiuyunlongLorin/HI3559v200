                    I2CWriteByte(DEV_ADDR, 0xd7,I2CReadByte(DEV_ADDR, 0xd7)|(1<<(ch%4)));

                    I2CWriteByte(DEV_ADDR, 0xFF, 0x02);
                    tmp = I2CReadByte(DEV_ADDR, 0x16+(ch%4)/2);
                    I2CWriteByte(DEV_ADDR,0x16+(ch%4)/2, (tmp&(ch%2==0?0xF0:0x0F))|(0x05<<((ch%2)*4)));

